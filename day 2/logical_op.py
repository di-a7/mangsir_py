# Logical operator:

# and : returns True if both statements are true otherwises False
a = 10
b = 20
c = 15

print(a > b  and a < c)

# or: returns True if one of the statements is true, False if both are false
print(a > b  or a < c)

# not: returns True if condition is False, vise versa
print(not(a > b))


