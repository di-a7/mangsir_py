# Comparision operators in Python: used to compare two values, Output: Boolean value

# equal to: ==    # if both values are same output is True, False otherwise
a = 10
b = 5
print(a == b)

# not equal to: !=  # if both values are not same output is True, False otherwise
print(a != b)

# greater than: >  # if a is greater than b output is True, False otherwise
# greater than or equal to: >= #  if a is greater or equal than b output is True, False otherwise

# less than: <       # if a is less than b output is True, False otherwise
# less than or equal to: <=       # if a is less than or equal b output is True, False otherwise


