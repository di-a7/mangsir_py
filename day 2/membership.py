# Membership operator: sequential datatype(groupdata, string), checks if a data exists in the sequential datatype, output: Boolean datatype

# in:                if a value or data exists in the sequential data then output is  True, otherwise False
data = 'mindriser'
print("d" in data)

data = ["a","ball","c","d"]
print("alksjdf" in data)

# not in          if a value or data exists in the sequential data then output is False, otherwise True
data = 'mindriser'
print("d" not in data)

a = "alksjdf"
data = ["a","ball","c","d"]
print(a not in data)


