# Operators: symbols used to perform operations of value or variables

# Arithmetic Operators: used to perform mathematical operations
a = 10
b = 5

# Addition : +
print(a + b)

# Subtraction : -
print(a - b)

# Multiplication : *
print(a * b)

# Division: /              # output float value
print(a / b)

# Floor Division : //      # output the integer part
print(a // b)

# Modulus : %              # outputs the remainder
print(a % b)

# Exponentiation : **      # power
print(a ** b)


