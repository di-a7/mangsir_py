# For loop

# iterable: sequential datatype(group datatype(list, tuple, set, dict), string datatype)
# iteration: the process of moving from first index to last index of the iterable
# iterator: is a variable that is used to perform iteration in iterable

# syntax
# for iterator in iterable:
   # statements

# in each loop i access a data from iterable in sequence
# for loop depends on the datas in sequential data
a = ["a","b",1 ,2, 3,4,5]
for i in a:            
   print("Hello world.",i)


